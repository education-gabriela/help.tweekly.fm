            <h1>Why do we need help?</h1>
<p>In December 2010 I wrote a simple script that would take the artists I listened to the most that week and post them to Twitter. Fast forward a few years and 15 million updates later, Tweekly.fm posts daily for thousands of people. I never could have dreamed that people working at Twitter, Google, Last.fm and other large tech companies would be using my tiny project.</p>

<p>The entire time the project has been running the development has been funded exclusively by me. Development time would easily run in to the hundreds of hours and support takes up around six hours a week on average. I've recently been writing the next version of tweekly.fm to include new profiles, publishing to tumblr/wordpress and other social networks and a better experience for our current users.</p>

<p>I have a full time job and previouly worked freelance to cover living and the costs associated with life. I'd like to be able to take a break from certain parts of work and be able to work solely on tweekly.fm. </p>

<p>Previously, when we've needed help with hosting, kind folks at Last.fm have stepped in. This isn't something that an external company can help us with although the service has been sponsored by my own company for hosting effectively. In the future, I hope that tweekly.fm can be self sustainable and eventually turn a profit. At that point, I could solely focus on it.</p>

<p>I'm writing this to ask you to donate anything you feel comfortable with towards the development of tweekly.fm and help us through this testing period. I would also like your views and opinions on what we should do next with tweekly.fm.</p>

<p>You can take our short survey using our link below and you can donate using the button below.</p>

<p>I'd like to place the list of donors on this site, if you do not wish to be listed then please email and let me know. As a thank you and a bonus, <strong>all donors will be given a lifetime account in the new version of tweekly.fm</strong>.</p>

<p>If there is anything you would like to discuss about the service, plans or ideas then please email me at <a href="mailto:support@tweekly.fm">support@tweekly.fm</a>.</p>
