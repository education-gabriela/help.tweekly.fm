            <h1>Warum brauchen wir Hilfe?</h1>
<p>Im Dezember 2010 habe ich ein einfaches Skript geschrieben, dass die Künstler, die ich in dieser Woche am meisten gehört hatte, auf Twitter postete. Mittlerweile ist es ein paar Jahre und 15 Millionen Updates später und tweekly.fm postet täglich für mehrere Tausend Benutzer. Ich hätte mir nie träumen können, dass Leute, die bei Twitter, Google, Last.fm und anderen großen Tech-Unternehmen arbeiten, mein winziges Projekt benutzen würden.</p>

<p>Die ganze Zeit in der das Projekt lief wurde die Entwicklung ausschließlich von mir finanziert. Die aufgewendete Entwicklungszeit liegt sicherlich bei einigen hundert Stunden und der Support benötigt im Durchschnitt 6 Stunden pro Woche. In letzer Zeit habe ich angefangen eine neue Version von tweekly.fm zu schreiben, die neben neuen Profilen auch Updates auf tumblr, Wordpress und anderen sozialen Netzwerken veröffentlichen kann und eine bessere Erfahrung für aktuelle Benutzer bietet.</p>

<p>Ich habe einen Vollzeit-Job und habe vorher freiberuflich gearbeitet um mein Leben und meinen Lebensunterhalt zu finanzieren. Ich hätte gerne die Möglichkeit bestimmte Bereiche der Arbeit auf Eis zu legen damit ich mich nur um tweekly.fm kümmern kann.</p>

<p>Früher, als wir Hilfe beim Hosting brauchten, sind freundliche Leute bei Last.fm eingesprungen. Bei so einem Projekt kann uns keine externe Firma helfen, obwohl der Dienst von meiner eigenen Firma für effektives Hosting gesponsort wurde. Ich hoffe, dass tweekly.fm sich in Zukunft selbst tragen kann und schlussendlich profitabel wird. An diesem Punkt könnte ich mich rein darauf fokussieren.</p>

<p>Ich schreibe dies um dich zu bitten einen Betrag, welchen du gerne bereit bist zu geben, für die Entwicklung von tweekly.fm zu spenden und uns durch diese Testphase zu helfen. Außerdem wüsste ich gerne deine Meinungen und Wünsche was wir als nächstes mit tweekly.fm tun sollen.</p>

<p>Über den Link unten kannst du eine kurze Umfrage (englisch) ausfüllen und über die Buttons unten kannst du spenden.</p>

<p>Ich würde gerne die Liste aller Spender auf diese Seite setzen. Wenn du nicht hier erscheinen möchtest, sende bitte eine kurze Email und lass es mich wissen. Als Dankeschön und als Bonus bekommen <strong>alle Spender einen Account auf Lebenszeit in der neuen Version von tweekly.fm</strong>.</p>

<p>Wenn es etwas gibt, was du über den Dienst, Pläne oder Ideen besprechen willst, sende mir bitte eine Email an <a href="mailto:support@tweekly.fm">support@tweekly.fm</a>.</p>
