            <h1>¿Por qué necesitamos tu ayuda?</h1>
<p>En diciembre de 2010 escribí un simple programa que tomaría mis artistas más escuchados de la semana y lo publicaría en Twitter. Unos años y 15 millones de actualizaciones después, Tweekly.fm publica diariamente para miles de personas. Nunca hubiera imaginado que gente trabajando en Twitter, Google, Last.fm y otras empresas grandes de tecnología usarían mi pequeño proyecto.</p>

<p>Todo este tiempo el proyecto ha sido desarrollado y financiado exclusivamente por mí. El tiempo de desarrollo sobrepasa fácilmente las cientos de horas y el soporte toma alrededor de seis horas a la semana en promedio. Recientemente, he estado programando la próxima versión de tweekly.fm para que incluya nuevos perfiles, publicaciones en tumblr y Wordpress y otras redes sociales, así como una mejor experiencia para los usuarios actuales.</p>

<p>Tengo un trabajo a tiempo completo además de trabajar como autónomo para cubrir mis gastos de vida. Quisiera poder dejar algunos trabajos a un lado y dedicar mi tiempo exclusivamente a tweekly.fm.</p>

<p>Anteriormente, cuando hemos necesitado ayuda para el alojamiento, gente muy amable en Last.fm ofreció su ayuda. Esto no es algo que una compañía externa pueda resolver, aunque el servicio de alojamiento haya sido efectivamente financiado por mi propia compañía en su totalidad. En el futuro, espero que Tweekly.fm pueda ser independiente y sostenible y eventualmente genere ganancias. En ese punto, podría enfocarme exclusivamente en ello.</p>

<p>Escribo esto para pedirte que dones cualquier monto que consideres correcto para el desarrollo de tweekly.fm y nos ayudes con el período de pruebas. También me gustaría conocer tu visión y tus opiniones en cuanto a lo que debemos hacer a continuación con tweekly.fm</p>

<p>Puedes tomar una encuesta corta o donar utilizando los enlaces en la parte de abajo.</p>

<p>Me gustaría publicar la lista de colaboradores en el sitio pero si no deseas aparecer en la lista, envíame un correo electrónico y házmelo saber. Para darte las gracias, todos los colaboradores recibirán una cuenta de por vida en la nueva versión de tweekly.fm.</p>

