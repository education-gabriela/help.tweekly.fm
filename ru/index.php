            <h1>Почему нам нужна помощь?</h1>
<p>В декабре 2010 я написал простой скрипт, собирающий список артистов, которых я слушал большего всего на неделе, и отправляет это в Твиттер. Спустя несколько лет и 15 миллионов постов, Tweekly.fm постит каждый день для тысяч людей. Я никогда не мечтал о том, чтобы люди, работающие в Twitter, Google, Last.fm и в других больших технологических компаниях пользовались бы моим небольшим проектом.</p>

<p>Все время проект поддерживался только мною. Разработка заняла сотни часов, а поддержка занимает в среднем около 6 часов в неделю. Я недавно написал следующую версию tweekly.fm для включения новых профилей, возможности постить в Tumblr/Wordpress/других соц.сетей и лучшего опыта для текущих пользователей.</p>

<p>У меня есть постоянная работа, также я работаю фрилансером, чтобы покрыть расходы. Я очень хочу взять перерыв от работы и полностью посвятить себяtweekly.fm.</p>

<p>Раньше, когда нам была нужна помощь с хостингом, добрые люди в Last.fm вступились. Это не то, чем внешняя компания может помочь нам, хотя сервис хостится на серверах моей кампании. В будущем я надеюсь, что tweekly.fm сможет быть независимым, и со временем даже будет приносить выгоду. Только с этой точкой зрения я могу полностью сосредоточиться на проекте.</p>

<p>Я пишу это, чтобы попросить вас поддержать проект для развития tweekly.fm и помочь нам в период тестирования. Мне также будет приятно услышать ваше мнение о том, что нам нужно будет сделать в следующей версииtweekly.fm.</p>

