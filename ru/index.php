<p>Мне хочется сделать сделать список поддержавших проект людей на сайте, но если вы не хотите быть в этом списке, пожалуйста, напишите мне на почту. В качестве благодарности все меценаты получат пожизненный аккаунт на новой версии tweekly.fm.</p>

